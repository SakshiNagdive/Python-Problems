#WAP for Add,Sub,Mul,Div and Rem

Num1 = int(input("Enter the number one:"))
Num2 = int(input("Enter the number two:"))

Add = Num1 + Num2
print("Addition of two numbers is :", Add)

Sub = Num1 - Num2
print("Subtraction of two numbers is :",Sub)

Mul = Num1 * Num2
print("Multiplication of two numbers is:" ,Mul)

Div = Num1 / Num2
print("Division of two numbers is:", Div )

Rem = Num1 % Num2
print("Reminder of Two numbers is",Rem)